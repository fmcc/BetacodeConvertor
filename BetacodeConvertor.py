
class Trie():
    
    def __init__(self):
        self.root = dict()
        self.match = True

    def add(self, key, value):
        trie_dict = self.root
        for char in key:
            trie_dict = trie_dict.setdefault(char, {})
        trie_dict = trie_dict.setdefault(self.match, value)

    def find(self, key):
        trie_dict = self.root
        unconverted = key
        for char in key:
            try:
                trie_dict = trie_dict[char]
            except KeyError:
                return trie_dict.get(self.match), unconverted
            unconverted = unconverted[1:]
        try:
            return trie_dict.get(self.match), unconverted
        except KeyError:
            return None

    def show_me(self):
        print(self.root)

